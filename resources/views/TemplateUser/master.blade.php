        $(document).ready(function () {
            var span = document.getElementById('span');

            function time() {
                var d = new Date();
                var y = d.getFullYear();
                var m = d.getMonth();
                var dd = d.getDate();
                var s = d.getSeconds();
                var m = d.getMinutes();
                var h = d.getHours();
                span.textContent = y + "-" + m + "-" + dd + " " + h + ":" + m + ":" + s;
            }

            setInterval(time, 1000);
        });
